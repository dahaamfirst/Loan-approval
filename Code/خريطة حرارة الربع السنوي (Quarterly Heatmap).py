# بيانات افتراضية للموافقات
data = pd.DataFrame({
    'المنطقة': ['حضرية', 'حضرية', 'حضرية', 'حضرية',
               'ريفية', 'ريفية', 'ريفية', 'ريفية',
               'شبه حضرية', 'شبه حضرية', 'شبه حضرية', 'شبه حضرية'],
    'الربع': ['Q1', 'Q2', 'Q3', 'Q4'] * 3,
    'نسبة الموافقة': [0.75, 0.78, 0.70, 0.82,
                   0.68, 0.72, 0.65, 0.80,
                   0.72, 0.75, 0.68, 0.78]
})

pivot = data.pivot("المنطقة", "الربع", "نسبة الموافقة")

plt.figure(figsize=(10, 6))
sns.heatmap(pivot, annot=True, fmt=".0%", cmap="YlGnBu", 
            linewidths=.5, cbar_kws={'label': 'نسبة الموافقة'})

# إضافة تأثيرات
plt.title("معدلات الموافقة حسب المنطقة والربع", pad=20)
plt.xticks(rotation=0)
plt.yticks(rotation=0)